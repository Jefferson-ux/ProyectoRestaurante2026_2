    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
                                          
    int fila = table.getSelectedRow();

    if (fila != -1) {
        // 1. Capturamos temporalmente
        String dniTmp = table.getValueAt(fila, 0).toString();
        String nomTmp = table.getValueAt(fila, 1).toString();
        String apeTmp = table.getValueAt(fila, 2).toString();

        // 2. Confirmación
        int aceptado = JOptionPane.showConfirmDialog(this, 
                "¿Desea seleccionar a este cliente?\n" +
                "DNI: " + dniTmp + "\n" +
                "Nombres: " + nomTmp + "\n" +
                "Apellidos: " + apeTmp,
                "Confirmar selección", JOptionPane.YES_NO_OPTION);

        // 3. Solo si acepta, guardamos en las propiedades públicas y cerramos
        if (aceptado == JOptionPane.YES_OPTION) {
            this.dniProp = dniTmp;
            this.nombreProp = nomTmp;
            this.apellidoProp = apeTmp;
            this.seleccionado = true; // AHORA SÍ es oficial
            this.dispose(); 
        } 
        // Si elige "No", no hacemos nada (seleccionado sigue false), 
        // permitiéndole elegir otra fila.

    } else {
        this.seleccionado = false;
        JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla.");
    }

    }//GEN-LAST:event_btnElegirActionPerformed

