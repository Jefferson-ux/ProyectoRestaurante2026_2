    public void llenarTablaBuscador() {
        table.setAutoCreateRowSorter(true);        
        //Limpiar la tabla antes de mostrar nuevos datos
        model.setRowCount(0);
        try {
            //Llama al método que retorna los datos de plato menues
            ResultSet rs = this.methods.listarCliente(); 
            while (rs.next()) {
                Object[] fila = {
                    rs.getString("DNI"),
                    rs.getString("Nombre de Cliente"),
                    rs.getString("Apellido de Cliente")
                };
                model.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar los resultados:\n" + e.getMessage(),
                    "ErrorDeConsulta", JOptionPane.ERROR_MESSAGE);
        }
    }


    
    
    
     public void buscarEnLaTabla() {
        String parametro = txtBuscar.getText().trim();
        if (parametro.isEmpty()) {
            parametro = "";
        }
        table.setAutoCreateRowSorter(true);   
        model.setRowCount(0);
        try {
            ResultSet rs = this.methods.buscarCliente(parametro);
            
            while (rs.next()) {
                Object[] fila = {
                    rs.getString("DNI"),
                    rs.getString("Nombre de Cliente"),
                    rs.getString("Apellido de Cliente")
                };
                model.addRow(fila);
            }


        } catch (SQLException e) {
            System.err.println("Error silencioso en búsqueda: " + e.getMessage());
        }
    }
    
    
    
    
    
    
    

